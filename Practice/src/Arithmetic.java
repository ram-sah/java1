public class Arithmetic {
    public static void main(String[] args) {
        int a = 10;
        int b = 6;
        System.out.println(a+b);
        System.out.println(a*b);
        System.out.println(a/b);
        double x = 10;
        float y = 6;
        System.out.println(x+y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println("modulus give remainder : ");
        System.out.print(x%y);


    }
}