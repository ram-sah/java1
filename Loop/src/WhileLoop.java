public class WhileLoop {
    public static void main(String[] args) {
        int num = 1;
        while (num < 10) {
            System.out.println(num);
            num++;
        }
//  decrement
        int num1 = 10;
        while (num1 >= 1) {
            System.out.println(num1);
            num1--;
        }
    }
}
